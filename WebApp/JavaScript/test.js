function changeText()
{
    var myText = document.getElementById("textbox");
    myText.style.color = "white";
    myText.style.fontSize = "14pt";
    myText.style.fontFamily = "Comic Sans MS";
    myText.style.fontWeight = "4px";
    myText.style.padding = "2px";
    myText.style.backgroundColor = "blue";
    myText.style.borderWidth = "2px";
}